<p><strong>七、保证金规则</strong></p>
<p>保证金旨在保证社区店向平台消费者提供优质的商品和服务质量。约购平台社区店应根据平台要求缴纳保证金，作为其侵权、违约、违规、未履行服务承诺时用于对消费者和其他权利人进行赔偿的资金。约购平台收取保证金，是依据国家电商法所规定的。您缴纳保证金，是约购平台对消费者权益的保护为促进约购平台更好地服务消费者。</p>
<p>1.根据商品类目的不同，予以缴纳保证金。保证金金额会根据约购平台的发展需要和对消费者权益保护的需要进行调整。</p>
<p>2.约购平台具有商品类目的判断和确认权利，缴纳保证金者不得以其它平台对类目的判断和确认为由拒绝或少缴纳保证金。</p>
<p>3.约购社区店应按平台规定统一缴纳保证金。若开设自营业务，根据自营商品所属类别对应保证金金额，采取差额不足方式补缴。若商品对应保证金低于社区店保证金，则店铺保证金仍以社区店保证金为准。</p>
<p>4.对于社区店主所缴存的保证金，约购平台不需向社区店主支付任何利息。</p>
<p>5.跨类目保证金：保证金缴纳采取就高原则，保证金按最高金额的类目缴纳；经营过程中增加的类目对应的保证金与原有保证金不一致，社区店主须补交差额部分。</p>
<p>6.约购平台有权根据社区店的集约商品变化及实际赔付情况等以书面方式通知社区店调整保证金金额。</p>
<p>7.如果社区店店铺受到行政机关调查处罚，约购平台有权提高社区店的保证金金额。</p>
<p>8.保证金必须支付到约购平台指定的账户，非约购平台指定账户，任何个人、组织、单位收取保证金，均不代表约购平台，社区店主必须充分判断和理解保证金支付账户，否则造成的保证金损失，与约购平台无关。社区店主及约购平台可有权追究非约购平台收取保证金的个人、组织、单位的法律责任。</p>
<p><strong><strong>10</strong></strong><strong><strong>、</strong></strong><strong><strong>保证金赔付</strong></strong></p>
<p>若经约购平台审核认定社区店有下列情形之一的，约购平台有权根据实际情况从社区店已缴付的保证金中扣除有关的款项赔付消费者或约购平台或相关权利人：</p>
<p>1）未履行或延迟履行任何服务承诺；</p>
<p>2）出售假货或出售质量不合格商品；</p>
<p>3）虚假或夸大宣传；</p>
<p>4）侵犯知识产权、财产权、人身权利等权利；</p>
<p>5）标错价无法发货；</p>
<p>6）对于消费者针对约购社区店服务承诺的每一有效投诉，约购平台将按货款金额的100%在保证金中做出标识予以暂扣，有效投诉判断，以约购平台确认为准；</p>
<p>7）其他违反约购平台规则、与约购签订的协议或相关的服务承诺的情形。</p>
<p>8）约购平台若提取社区店的保证金进行任何赔付，将以电子邮件或短信等形式通知社区店主，并说明赔付原因及赔付金额。</p>
<p>9）通过商品价格、文字、图片、音频、视频等形式，给约购平台或约购平台其它商家、社区店造成一定的直接或间接经济损失。</p>
<p>10）因司法机关要求冻结保证金的，约购平台必须冻结。</p>
<p>2.保证金不足的情形处理：</p>
<p>1）当社区店的保证金不足以赔付消费者及其他权利人时，社区店应自行支付额外的赔付金额；</p>
<p>2）如社区店的保证金不足以支付任何赔付金额，约购平台有权中止或终止服务，包括但不限于采取冻结社区店应结货款、将社区店从平台清退、追究社区店的违约责任等；</p>
<p>3）在社区店的保证金不足时，约购平台没有义务使用自有资金向消费者或其他权利人支付赔偿金、补偿金或其它任何款项；</p>
<p>4）如社区店违反有关的服务承诺和/或约购平台规则和/或社区店与约购平台签署的其他协议，给约购平台造成任何损失（包括但不限于诉讼赔偿、诉讼费用、律师费用等）的，约购平台有权从保证金中扣除相当于约购损失的金额。</p>
<p><strong><strong>11.</strong></strong><strong><strong>保证金补缴</strong></strong></p>
<p>如因社区店的业务变化或集约商品需补缴或保证金可用余额低于保证金总额的50%，社区店应在出现该情况后5个自然日内及时补缴足额的保证金使可用余额达至最低保证金金额。约购平台可向社区店发出续费通知，如社区店在约购开放平台上有未结货款，约购平台有权从该等款项中扣除以补足保证金。如社区店逾期未能补足保证金，则约购开放平台有权终止服务。</p>
