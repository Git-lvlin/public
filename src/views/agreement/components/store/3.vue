<p><strong>约购平台社区店规则及服务须知3.0</strong></p>
<p>&nbsp;</p>
<p>欢迎您入驻约购平台，成为社区店卖家，即社区店店主。在您正式开通社区店之前，为保障您的权益，请认真阅读本规则及服务须知。如您勾选&ldquo;已阅读&rdquo;则代表您理解并同意本规则及须知内容，如有异议请终止入驻。</p>
<p>约购平台的各项服务协议及规则、须知、通知等文件，最终解释权归约购平台所有。</p>
<p><strong><strong>一、本规则相关定义：</strong></strong></p>
<p>1、约购平台：指约购平台移动客户端软件、小程序及其他约购应用程序。</p>
<p>2、社区店：指的是用户通过对约购平台的了解，自愿向约购平台申请在某小区/或社区/或村乡镇（以下统称&lsquo;社区&rsquo;）设立的约购社区店（以下简称&lsquo;社区店&rsquo;），并通过约购平台审核通过，主要服务于所在社区用户的约购平台和集约商品配送及协助平台的相关服务（一个社区可以一个或多个社区店），缴纳平台年服务费的店主升级为VIP店主。</p>
<p>3、VIP店主权益：享有平台制定的最优推广权益和分享服务佣金收益。</p>
<p><strong><strong>二、社区店入驻流程</strong></strong></p>
<p>1、注册账号；</p>
<p>2、手机号验证码；</p>
<p>3、开通支付通道；</p>
<p>5、填写开店申请资料；</p>
<p>6、平台审核，如审核通过，可以开始经营约购社区店。</p>
<p><strong>三</strong><strong>、社区店职能与服务</strong></p>
<p>1、建立私域流量池，包括小区邻居群、个人亲友群及其他社群。</p>
<p>2、培养和维护社区平台消费者。</p>
<p>3、从平台的集约中心选品，在小区内组织集约、收货及配送服务。</p>
<p>4、在做好第一个社区店的条件下，拓展新的社区店。</p>
<p>5、如社区店开展与平台业务发生冲突、产生竞争性业务时，平台有权终止社区店的服务。</p>
<p>6、严格遵守平台物流配送的要求，按照约购平台集约商品的发货、配送规则执行。</p>
<p>7、严格履行约购社区店的义务，无论您因为何种原因而失联，当您失联超过12小时，导致已经下单的约购平台集约商品消费者尚未收到货或未发货的商品的情形出现，由此产生的用户损失由店主承担，且约购平台有权根据消费者要求从您的账户里扣取相关款项以给予消费者补偿或退款。</p>
<p>8、当您因特殊情况暂不能履行服务义务，须事先告知约购平台，否则视其情节轻重，分别给予警告或按照约购平台的规则予以处罚。</p>
<p><strong><strong>四</strong></strong><strong><strong>、集约模式规则</strong></strong></p>
<p>约购平台为您提供集约的约购方式，主要向平台的社区平台消费者开放，具有一定起订量要求，价格一定的价格优势。平台已为店主设立了集约中心或其他板块，备有各品类集约商品样品、特价商品、散装批发商品。店主可从集约中心直接选取意向商品并予以集采，集约模式核心要素如下：</p>
<p>1.集约仅对店主和运营中心开放。</p>
<p>2.平台集约中心将定期/不定期更新产品库，店主可在集约中心内决定挑选和采购集约商品销售。</p>
<p>3.因价格优势基于大额订单，集约对订单有保底数量要求，低于保底量无法生成有效订单，不同品类保底量要求见商品详情页。</p>
<p>4.店主从集约中心选品下单后，需支付全款至平台结算中心，该集约订单视为启动。</p>
<p>5.集约订单启动后进入组团期，同时店主应在组团时间内组织本社区平台消费者的订购，满足该产品订单量。社区组团时间由平台卖家（或平台代运营）设置。</p>
<p>6.集约期间，集约商品送达至约购社区店后，平台消费者可在约购平台约购社区店下单购买，店主在闭团后向平台提交订单信息。</p>
<p>7.店主负责授权区域的社区的（村镇的统一简称为社区）平台消费者的商品自提或配送，如为店主配送，则平台为店主提供配送费补贴（仅限平台普适品），补贴标准以平台规定为准。</p>
<p>8.每个集约订单只允许您指定一个收货地址，不做拆分。由您负责社区内配送工作，平台将为您提供配送费补贴。如您需将一个集约订单配送至多个收货地址，供应商或区域运营中心可配合，但增加的物流费用由您个人承担。</p>
<p>9.社区店的货款支付与账务管理，按照平台的规定执行。</p>
<p><strong><strong>五</strong></strong><strong><strong>、</strong></strong><strong><strong>社区店的佣金利益分享</strong></strong></p>
<p>1、平台佣金制度：平台采取单项收佣制度，向商品卖家收取交易额的一定比例作为平台佣金；</p>
<p>2、交易佣金分享：社区店可从为平台带来的交易佣金收入中得到一定比例的服务佣金。</p>
<p>3、约购社区店&rdquo;的门店不得与任何品牌联合挂牌 （甲方特殊审批或甲方合作单位除外）。如有特例社区店店主履行约购平台社区店卖家服务、遵守约购平台的各项规则、须知、通知等性质不改变。社区店的店主、VIP店主均应履行国家税务规定，正常缴纳个人所得税等税费。</p>
<p>4、社区店的佣金计算及发放方式，根据集约商品的不同品类以平台规定的佣金比例及发放方式为准。</p>
<p><strong><strong>六</strong></strong><strong><strong>、</strong></strong><strong><strong>收、发货规则</strong></strong></p>
<p>1、约购集约商品的收货方式：</p>
<ul>
<li>店主应对到货的集约商品按照约购平台集约商品清单核对商品名称、种类、商品数量、规格，并对外包装是否损坏进行检查、核对；</li>
<li>如有集约商品受损必须将拍摄的商品的照片、视频、送货单照片上传并发送至运营中心客服；</li>
<li>如有供应商直发商品受损必须将拍摄的商品的照片、视频、送货单照片上传并发送至约购平台客服；</li>
<li>当实际送货数量小于应到商品数量，按照实际数量签收入库（需保留送货单据，并对送货单拍照留底）后，应立即与约购平台客服（供应商直发）或运营中心客服（运营中心配送）联络，并对已标注实际数量的送货单照片发送至约购平台客服（供应商直发）或运营中心客服（运营中心配送）。</li>
</ul>
<p>2、发货方式</p>
<p>当店主向平台消费者出售集约商品后，您必须积极完成配送服务，配送方式包括：</p>
<p>1）配送：社区&ldquo;集约&rdquo;最主要的发货方式，指由您或您指定的人员负责在社区内自行完成配送；</p>
<ul>
<li>自提：如您与消费者提前协商一致，可由消费者根据您指定地点&ldquo;上门提货&rdquo;，或消费者在约购平台下单时选择&lsquo;自提&rsquo;，即由消费者按选择的自提点取货；</li>
<li>第三方物流配送：指您通过&ldquo;第三方物流配送&rdquo;向消费者进行配送，因此产生的物流费用或商品损耗由您承担。</li>
<li>发货包装管理</li>
</ul>
<p>发货包装中（包括但不限于外包装盒、内部夹页、退换货页、封箱胶等），商家可以增加约购平台店铺链接及二维码链接等信息；发货包装中不得有其他平台的店铺信息（包括但不限于链接、二维码、联系信息），或与该店铺商品无关的信息。</p>
<p>4、发货时效</p>
<p>您应在收到商品的24小时内配送给消费者下单指定地址。由您负责配送至终端客户地址,若终端客户始终无法联系，应及时上传后台系统，后台将向客户发送信息。</p>
<p>5、营销活动及节假日发货要求</p>
<p>约购平台官方发起的促销活动及特定节假日的配送时效要求，以平台通知或公告为准。</p>
<p>6、违规处理</p>
<p>1）延迟发货</p>
<p>如您未按约定时间内将产品配送至平台消费者，视为您延迟配送。</p>
<p>2）虚假/欺诈发货</p>
<p>如您收到配送的集约商品，但平台消费者在合理期限内未实际收到所购商品的或私自调换商品的，向平台消费者配送非平台供应产品的，平台将判定为虚假/欺诈配送，平台及消费者将追究您的责任。约购平台对商家虚假发货每单处罚订单金额的10倍，平台有权视严重程度强制关停店铺。</p>
<p><strong><strong>七、</strong></strong><strong><strong>约购平台商品退换货规则</strong></strong></p>
<p>1、约购平台为消费者提供7天无理由退换货服务。除特殊产品品类和集约商品外，平台销售产品品类均在七天无理由退换货保障涵盖范围内，具体参见商品详情页。不适用七天无理由退换货的品类包括：</p>
<p>1）消费者定制商品，例如个性定制、设计品；</p>
<p>2）鲜活易腐商品，例如新鲜水果等；</p>
<p>3）在线下载或消费者拆封的音像制品、计算机软件等数字化商品；</p>
<p>4）一经激活或试用后大幅贬值的产品，例如智能电子产品；</p>
<p>5）拆封后影响人身安全/健康，或易导致商品品质改变的产品，例如食品、保健品、化妆品、贴身用品等；</p>
<p>6）约购平台集约商品因属统一定制、批量发货，不属于无理由退货之列。</p>
<p>2、七天无理由退换货范围内的商品，符合以下条件，在商品物流显示签收七日内，可以要求商家退换货：</p>
<p>1）客户在收到货物时当面在送货员面前拆包检查，发现货物有质量问题的；</p>
<p>2）实际收到货物与网站上描述的有很大的出入的；</p>
<p>3）卖家发错货、发错尺码规格；</p>
<p>4）对货物不满意或不喜欢的（在保证没使用及包装配件全的情况下）。</p>
<p>3、以下情况，商家拒绝退换货：</p>
<p>1）商品平台消费者签收超出7天或者已经使用的商品将不在退货范围，对于已经使用的商品，不予以退换货；</p>
<p>2）退换货物不全或者外观受损，影响二次销售的产品。</p>
<p>5、退换货流程说明</p>
<p>1）及时与客服人员联系，说明退货理由并填交退换货申请；</p>
<p>2）将货物按客服人员提供的地址寄回；</p>
<p>3）商家确认收到货物后，如果是换货将重新安排发货，如是退货将消费者付款路径原路退回。</p>
<p>6、货物流费用说明</p>
<p>因签收时损坏、运营中心错发、漏发的换货、补货，由运营中心承担运费。</p>
