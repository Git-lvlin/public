<p><strong>八、保证金规则</strong></p>
<p>保证金旨在保证社区店向平台消费者提供优质的商品和服务质量。约购平台社区店应根据平台要求缴纳保证金，作为其侵权、违约、违规、未履行服务承诺时用于对消费者和其他权利人进行赔偿的资金。约购平台收取保证金，是依据国家电商法所规定的。您缴纳保证金，是约购平台对消费者权益的保护为促进约购平台更好地服务消费者。</p>
<p>1、根据商品类目的不同，予以缴纳保证金。保证金金额会根据约购平台的发展需要和对消费者权益保护的需要进行调整。</p>
<p>2、约购平台具有商品类目的判断和确认权利，缴纳保证金者不得以其它平台对类目的判断和确认为由拒绝或少缴纳保证金。</p>
<p>3、约购社区店应按平台规定统一缴纳保证金。若开设自营业务，根据自营商品所属类别对应保证金金额，采取差额不足方式补缴。若商品对应保证金低于社区店保证金，则店铺保证金仍以社区店保证金为准。</p>
<p>4、对于社区店主所缴存的保证金，约购平台不需向社区店主支付任何利息。</p>
<p>5、店铺关闭后，平台将按照平台规则之规定，退还商家保证金。</p>
<p>6、跨类目保证金：保证金缴纳采取就高原则，保证金按最高金额的类目缴纳；经营过程中增加的类目对应的保证金与原有保证金不一致，社区店主须补交差额部分。</p>
<p>7、约购平台有权根据社区店的集约商品变化及实际赔付情况等以书面方式通知社区店调整保证金金额。</p>
<p>8、如果社区店店铺受到行政机关调查处罚，约购平台有权提高社区店的保证金金额。</p>
<p>9、保证金必须支付到约购平台指定的账户，非约购平台指定账户，任何个人、组织、单位收取保证金，均不代表约购平台，社区店主必须充分判断和理解保证金支付账户，否则造成的保证金损失，与约购平台无关。社区店主及约购平台可有权追究非约购平台收取保证金的个人、组织、单位的法律责任。</p>
<p>10、保证金赔付若经约购平台审核认定社区店有下列情形之一的，约购平台有权根据实际情况从社区店已缴付的保证金中扣除有关的款项赔付消费者或约购平台或相关权利人：</p>
<p>1）未履行或延迟履行任何服务承诺；</p>
<p>2）出售假货或出售质量不合格商品；</p>
<p>3）虚假或夸大宣传；</p>
