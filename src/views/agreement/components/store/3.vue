<p>2、平台年服务费以约购平台在每个阶段制定的收费金额为标准，并以平台显示的金额支付为实际有效。</p>
<p>3、平台服务费按年进行收取（自签署《约购平台服务协议》之日起计算第1年），第二年及以后年度平台服务费，社区店主需在协议有效期届满前30天续费，如一年期限届满仍未续费，则社区店资格失效（包括VIP店主权益失效）。</p>
