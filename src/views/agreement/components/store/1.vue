<li>平台审核申请通过，放入该单品集约待启动池；</li>
</ul>
<p>3）该产品集约池订单量达到启动标准（平台设置）；</p>
<p>4）平台将其发布为集约产品，进入常规集约流程；</p>
<p>5）主动集约成为常规集约，成绩计入社区店的考核指标。</p>
