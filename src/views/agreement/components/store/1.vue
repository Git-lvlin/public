<p><strong><strong>一</strong></strong><strong><strong>、集约模式说明</strong></strong></p>
<p>集约为约购平台向社区店店主（下称&lsquo;店主&rsquo;）提供的一种约购方式，该方式通过汇集大额订单增强与供应商的议价能力，将同品质商品的价格压至全平台最优。平台已为店主设立了采购中心，备有各品类商品，供应商均已认可平台集约方式。店主可从采购中心直接选取意向商品及供应商，免除大量货源沟通成本。集约模式核心要素如下：</p>
<p>1.集约仅对店主开放；</p>
<p>2.平台供货中心将定期/不定期更新产品库，店主可在供货中心内决定挑选销售产品；</p>
<p>3.因价格优势基于大额订单，集约对订单有保底数量要求，低于保底量无法生成有效订单，不同品类保底量要求见商品详情页；</p>
<p>4.店主从采购中心选品下单后，需支付30%定金至平台结算中心，该集约订单视为启动。</p>
<p>5.集约订单启动后进入组团期，平台向供应商支付订金，供应商备货生产。同时店主应在组团时间内组织本社区C端用户的订购，满足该产品订单量。社区组团时间由平台卖家（或平台代运营）设置；</p>
<p>6.组团期间，参团的C端用户订购即付全款给店主。店主在闭团后向平台提交订单信息（产品名称、数量、C端用户信息列表、收货地址等），并将所收款项转至平台集约订单归集专门账户。供应商安排发货，发货周期视商品库存情况确定，平台统一向供应商付尾款；</p>
<p>7.供应商负责将产品运送至店主指定收货地址，一个集约订单配送一个地址，不做拆分。店主负责本小区内（村镇的统一简称为本小区）用户的配送，平台为店主提供配送费补贴，补贴标准以平台规定为准。</p>
