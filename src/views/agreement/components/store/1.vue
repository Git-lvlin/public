<p>2.主动集约：</p>
<p>在平台发布的集约产品之外，您可在采购中心主动选择自己决定发起集约的产品，向平台提交申请，当平台约购池形成一定基础量后，平台将自动生成新一期的集约产品，算作任务指标。主动集约的流程：</p>
<ul>
