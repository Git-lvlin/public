<p>3.集约闭团后，您应向平台提交订单信息（产品名称、数量、C端用户订购情况、您指定的收货地址等），并将所收全部款项转至平台集约订单归集专门账户；</p>
<p>4.供应商按订单备货，发货至您指定的一个收货地址，通常以您向约购平台备案的社区店地址为准；</p>
<p>5.每个集约订单您可指定一个收货地址，一般情况下不做拆分。由您负责小区内配送工作，平台将为您提供配送费补贴。如您需将一个集约订单配送至多个收货地址，供应商可配合，但增加的物流费用由您个人承担。</p>
<p><strong><strong>三</strong></strong><strong><strong>、会员店的三种</strong></strong><strong><strong>集约</strong></strong><strong><strong>方式</strong></strong></p>
<p>您作为社区店店主，属于本平台的特定对象，本平台或商家发布的商品或者服务信息，即为要约条件，您选择该商品或者服务并提交定金或订单成功，该集约合同成立。本平台的集约方式符合要约条件，属于要约合同，非格式条款约定您支付价款。您在本小区向用户发起约购，其法律性质为要约合同。</p>
