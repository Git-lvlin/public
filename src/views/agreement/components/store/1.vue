<p><strong>约购平台集约模式规则说明</strong></p>
<p>&nbsp;</p>
<p>欢迎您使用约购平台服务！约购平台为您提供如下两种约购方式：</p>
<p>【秒约】：个人购买方式之一，无需组团、即时成交，同等产品价格大多数较主流购物网站优惠。</p>
<p>【集约】：主要向平台的社区用户开放，起订量高，价格最优。</p>
